//Way 2: Initialization of an 1D array

class OneDArray{
	
	public static void main(String[] args){
		
		int arr[] = {10,20,30,40,50};
		//int []arr = {10,20,30,40,50}; Valid syntax
		arr[0] = 11;
		arr[1] = 22;
		arr[2] = 33;

		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
	}
}
